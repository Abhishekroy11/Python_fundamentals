'''This is a simple addition program,
where it will add two string values because i do not mention its type(concatenation).'''
a=input("Enter the value of a:")
b=input("Enter the value of b:")
sum=(a)+(b)
print(sum)
'''using type casting we can convert string into integer value and then add them.'''
a=int(input("Enter the value of a:"))
b=int(input("Enter the value of b:"))
sum=a+b
print(sum)
'''This is conversion float.'''
a=float(input("Enter the value of a:"))
b=float(input("Enter the value of b:"))
sum=a+b
print(sum)  